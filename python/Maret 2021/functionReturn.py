def tambah(a, b):
    return a + b

def kurang(x, y):
    return x - y

def kali(p, q):
    return p * q

def bagi(c, d):
    return c / d

def modulus(e, f):
    return e % f

def pangkat(g, h):
    return g ** h

print("Hasil penjumlahan dari 2 + 2 =", tambah(2,2))
print("Hasil pengurangan dari 3 - 4 =", kurang(3,4))
print("Hasil perkalian dari   6 * 2 =", kali(6,2))
print("Hasil pembagian dari   8 / 4 =", bagi(8,4))
print("Hasil modulus dari     7 % 3 =", modulus(7,3))
print("Hasil pangkat dari    5 ** 4 =", pangkat(5,4))
